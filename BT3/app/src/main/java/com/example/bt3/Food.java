package com.example.bt3;

public class Food {
    String name;
    String id;
    String link;
    public String getId()
    {
        return id;
    }
    public void setId (String id)
    {
        this.id = id;
    }
    public String getName()
    {
        return name;
    }
    public void setName (String name)
    {
        this.name = name;
    }
    public String getLink()
    {
        return link;
    }
    public void setLink (String link)
    {
        this.link = link;
    }
}
